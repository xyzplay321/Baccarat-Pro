# 輔助函數：配置驗證
# ═══════════════════════════════════════════════════════════════════════════════

def validate_config():
    """驗證所有配置的合理性"""
    errors = []
    
    # 驗證 EOR 來源
    if DEFAULT_EOR_SOURCE not in EOR_SOURCES:
        errors.append(f"Invalid EOR source: {DEFAULT_EOR_SOURCE}")
    
    # 驗證策略
    if DEFAULT_BET_STRATEGY not in BET_STRATEGIES:
        errors.append(f"Invalid bet strategy: {DEFAULT_BET_STRATEGY}")
    
    # 驗證路單
    for road in ROAD_CONFIGURATIONS:
        if road not in ROAD_PREDICTION_WEIGHTS:
            errors.append(f"Road config mismatch: {road}")
    
    # 驗證百分比之和
    weight_sum = sum(ROAD_PREDICTION_WEIGHTS.values())
    if abs(weight_sum - 1.0) > 0.01:
        errors.append(f"Road weights don't sum to 1.0: {weight_sum}")
    
    if errors:
        raise ValueError(f"Configuration errors: {errors}")
    
    return True


